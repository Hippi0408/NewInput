#include "manager.h"
#include "game.h"
#include "input.h"
#include "texture.h"
#include <assert.h>
#include "2dpolygon.h"

//*****************************************************************************
// コンストラクタ
//*****************************************************************************
CGame::CGame()
{
}

//*****************************************************************************
// デストラクタ
//*****************************************************************************
CGame::~CGame()
{
}

//*****************************************************************************
// 初期化
//*****************************************************************************
HRESULT CGame::Init()
{
	
	m_p2DPolygon = new C2DPolygon;
	if (FAILED(m_p2DPolygon->Init()))
	{
		return -1;
	}
	
	m_p2DPolygon->SetTextIndex(0);
	m_p2DPolygon->SetPos(D3DXVECTOR3(SCREEN_WIDTH * 0.5f, SCREEN_HEIGHT * 0.5f, 0.0f));
	m_p2DPolygon->SetDiagonalLine(600.0f, 600.0f);
	m_p2DPolygon->SetPolygon();

	return S_OK;
}

//*****************************************************************************
// 終了処理
//*****************************************************************************
void CGame::Uninit()
{
	if (m_p2DPolygon != nullptr)
	{
		m_p2DPolygon->Uninit();
		delete m_p2DPolygon;
		m_p2DPolygon = nullptr;
	}
}

//*****************************************************************************
// 更新処理
//*****************************************************************************
void CGame::Update()
{
	
}

//*****************************************************************************
// 描画処理
//*****************************************************************************
void CGame::Draw()
{
	CInput* pInput = CInput::GetKey();


	if (pInput->Press(JOYPAD_X))
	{
		m_p2DPolygon->Draw();
	}
}
